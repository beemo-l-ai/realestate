#!/usr/bin/env node
import { execSync } from 'node:child_process';
import path from 'node:path';
import { clearLogs, readLogs } from './change-log.mjs';

function run(cmd, opts = {}) {
  return execSync(cmd, {
    stdio: ['ignore', 'pipe', 'pipe'],
    encoding: 'utf8',
    ...opts
  }).trim();
}

function runIgnoreError(cmd) {
  try {
    return run(cmd);
  } catch {
    return '';
  }
}

function unique(list) {
  return Array.from(new Set(list));
}

function inferType(intents, files) {
  const text = intents.join(' ').toLowerCase();
  const allDocs = files.length > 0 && files.every((f) => f.startsWith('docs/') || f.endsWith('.md'));

  if (allDocs || /(문서|docs|readme)/.test(text)) return 'docs';
  if (/(fix|bug|오류|버그|hotfix|문제)/.test(text)) return 'fix';
  if (/(refactor|리팩터|구조개선)/.test(text)) return 'refactor';
  if (/(test|테스트)/.test(text)) return 'test';
  if (/(ci|workflow|github action)/.test(text)) return 'ci';
  if (/(build|deps|dependency|chore|설정|환경)/.test(text)) return 'chore';
  if (/(add|추가|구현|신규|create)/.test(text)) return 'feat';
  return 'chore';
}

function inferScope(files) {
  if (files.length === 0) return '';
  const topDirs = unique(
    files
      .map((f) => f.replace(/^[./]+/, ''))
      .map((f) => f.split('/')[0])
      .filter(Boolean)
  );

  if (topDirs.length === 1) {
    const scope = topDirs[0].replace(/[^a-zA-Z0-9_-]/g, '');
    if (scope) return scope;
  }
  return '';
}

function buildSubject(intents) {
  const first = intents.find(Boolean) || 'apply tracked changes';
  const singleLine = first.replace(/\s+/g, ' ').trim();
  const subject = singleLine.length > 72 ? `${singleLine.slice(0, 69)}...` : singleLine;
  return subject || 'apply tracked changes';
}

function gitChangedFiles() {
  const porcelain = runIgnoreError('git status --porcelain');
  if (!porcelain) return [];

  return porcelain
    .split('\n')
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => {
      const file = line.slice(3).trim();
      if (file.includes(' -> ')) {
        return file.split(' -> ').pop();
      }
      return file;
    });
}

function pathExistsInRepo(file) {
  try {
    run(`git ls-files --error-unmatch -- "${file.replace(/"/g, '\\"')}"`);
    return true;
  } catch {
    try {
      run(`test -e "${file.replace(/"/g, '\\"')}" && echo ok`);
      return true;
    } catch {
      return false;
    }
  }
}

function stageFiles(files) {
  const valid = files.filter(pathExistsInRepo);
  if (valid.length === 0) {
    return [];
  }

  const quoted = valid.map((f) => `"${f.replace(/"/g, '\\"')}"`).join(' ');
  run(`git add -- ${quoted}`);
  return valid;
}

function createPr(commitHeader, commitBody) {
  const hasGh = runIgnoreError('command -v gh');
  if (!hasGh) {
    return { created: false, reason: 'gh CLI not found' };
  }

  const currentBranch = runIgnoreError('git rev-parse --abbrev-ref HEAD');
  if (!currentBranch || currentBranch === 'main' || currentBranch === 'master') {
    return { created: false, reason: 'current branch is main/master or unavailable' };
  }

  const base = runIgnoreError('git symbolic-ref refs/remotes/origin/HEAD | sed "s@^refs/remotes/origin/@@"') || 'main';
  const prBody = `${commitBody}\n\nGenerated by git-commit/commit-from-log.mjs`;
  const escapedTitle = commitHeader.replace(/"/g, '\\"');
  const escapedBody = prBody.replace(/"/g, '\\"');

  try {
    run(`gh pr create --base "${base}" --head "${currentBranch}" --title "${escapedTitle}" --body "${escapedBody}"`);
    return { created: true };
  } catch (error) {
    return { created: false, reason: error.message.split('\n')[0] };
  }
}

const logs = readLogs();
if (logs.length === 0) {
  console.error('No change logs found in .codex-changes/changes.jsonl');
  process.exit(1);
}

const intents = unique(
  logs
    .filter((x) => x.type === 'intent' && typeof x.intent === 'string')
    .map((x) => x.intent.trim())
    .filter(Boolean)
);

const loggedFiles = unique(
  logs
    .filter((x) => x.type === 'file_change' && Array.isArray(x.files))
    .flatMap((x) => x.files)
    .map((f) => String(f).trim())
    .filter(Boolean)
    .map((f) => path.relative(process.cwd(), path.resolve(process.cwd(), f)))
    .filter((f) => !f.startsWith('..'))
);

const changedNow = gitChangedFiles();
const stageCandidates = unique(loggedFiles.filter((file) => changedNow.includes(file)));
const staged = stageFiles(stageCandidates);

if (staged.length === 0) {
  console.error('No tracked changed files to stage from logs.');
  process.exit(1);
}

const stagedDiff = runIgnoreError('git diff --cached --name-status');
if (!stagedDiff) {
  console.error('No staged diff found after staging files.');
  process.exit(1);
}

const type = inferType(intents, staged);
const scope = inferScope(staged);
const subject = buildSubject(intents);
const header = `${type}${scope ? `(${scope})` : ''}: ${subject}`;

const bodyLines = [];
if (intents.length > 0) {
  bodyLines.push('Intent(s):');
  bodyLines.push(...intents.map((intent) => `- ${intent}`));
}
bodyLines.push('');
bodyLines.push('Files:');
bodyLines.push(...staged.map((file) => `- ${file}`));

const body = bodyLines.join('\n').trim();

run(`git commit -m "${header.replace(/"/g, '\\"')}" -m "${body.replace(/"/g, '\\"')}"`);

const prResult = createPr(header, body);
clearLogs();

console.log(`Committed with message: ${header}`);
if (prResult.created) {
  console.log('PR created successfully.');
} else {
  console.log(`PR skipped: ${prResult.reason}`);
}
console.log('Cleared .codex-changes/changes.jsonl');
